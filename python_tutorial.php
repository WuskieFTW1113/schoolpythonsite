        <h3>2. Python Syntax</h3>
        <p>Python uses a clean and easy-to-read syntax, which makes it an ideal language for beginners. It uses indentation and whitespace to define code blocks, eliminating the need for curly braces. This makes Python code highly readable and reduces the likelihood of syntax errors.</p>
        
        <h3>3. Variables and Data Types</h3>
        <p>In Python, you can store data in variables. Python is dynamically-typed, meaning you don't need to declare the type of a variable. It automatically determines the type based on the value assigned. Common data types in Python include integers, floats, strings, lists, and dictionaries.</p>
        
